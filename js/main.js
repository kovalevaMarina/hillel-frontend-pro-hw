/*
  Задача 1. В переменной min лежит число от 0 до 59. Определите в какую четверть 
  часа попадает это число (в первую, вторую, третью или четвертую).
*/
var min = +prompt("Введите число от 0 до 59");

if (min > 0 && min <= 15) {
  alert("Первая четверь часа");
} else if (min > 15 && min <= 30) {
  alert("Вторая четверь часа");
} else if (min > 30 && min <= 45) {
  alert("Третья четверь часа");
} else if (min > 45 && min <= 59) {
  alert("Четвертая четверь часа");
} else {
  alert("error");
}

