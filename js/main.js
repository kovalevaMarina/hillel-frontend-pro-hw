/*
  Задача 5. Переменная num может принимать 4 значения: 1, 2, 3 или 4. Если она имеет значение '1', то в 
  переменную result запишем 'зима', если имеет значение '2' – 'весна' и так далее.
*/

var num = +prompt("Введите любое значение 1, 2, 3 или 4");
var result;

if (num === 1) {
  result = "зима";
} else if (num === 2) {
  result = "весна";
} else if (num === 3) {
  result = "лето";
} else if (num === 4) {
  result = "осень";
} else {
  result = "error"
}

alert(result);
